"""
gen_sysml.py — Generate SysML v2 diagrams from C6 full-pipeline outputs
------------------------------------------------------------------------
Reads interactions.json and sequence.json from C6_full_pipeline/<scenario>/
and writes three SysML v2 files per scenario into:

    experiments/H1/conditions/sysml_diagrams/<scenario>/
        interactions.sysml  — structural architecture: component part defs,
                              port-interface defs, and connections between them
        sequence.sysml      — ordered execution: one SynchronousCall per step
        views.sysml         — InterconnectionView (structure) + SequenceView
                              declarations for the spec42 plugin

interactions.sysml pattern (structural, spec42 InterconnectionView):
    port def <Interface>           -- one per unique interaction name
    part def <Component> {         -- one per component
        port calls<X>    : <X>;   -- caller side
        port provides<X> : <X>;   -- callee side
    }
    part <scenario>System {        -- top-level system instance
        part <name> : <Component>;
        connection : <X> connect <src>::calls<X> to <tgt>::provides<X>;
    }

sequence.sysml pattern (interaction scenario, spec42 SequenceView):
    part def :> InteractionScenario
    part <name> : Lifeline
    part mN : SynchronousCall { ref from/to; label }

Notes from sequence.json (LLM provenance annotations) are rendered as
SysML line comments next to the relevant message.

Usage:
    python gen_sysml.py                   # all scenarios
    python gen_sysml.py --scenario S4     # single scenario
"""
import argparse
import json
import pathlib

# -- Paths ------------------------------------------------------------------
ROOT     = pathlib.Path(__file__).parent.parent.parent.parent   # cpscore/
H1_DIR   = ROOT / "experiments" / "H1"
C6_DIR   = H1_DIR / "conditions" / "C6_full_pipeline"
OUT_ROOT = H1_DIR / "conditions" / "sysml_diagrams"

ALL_SCENARIOS = ["S1a", "S1b", "S2", "S3", "S4"]

SCENARIO_LABELS = {
    "S1a": "Synchronized Runner -- happy path",
    "S1b": "Synchronized Runner Timeout -- failure path",
    "S2":  "Configuration Property Mapping",
    "S3":  "Multi-Component Runner Orchestration",
    "S4":  "Stage Event Bridge Pub-Sub Routing (constructed)",
}

# -- Helpers ----------------------------------------------------------------

def _ll(name):
    return name[0].lower() + name[1:]

def _cap(name):
    return name[0].upper() + name[1:]

def _load_json(path):
    if not path.exists():
        return []
    return json.loads(path.read_text())

def _ordered_unique(records, key):
    seen = []
    for r in records:
        v = r.get(key, "")
        if v and v not in seen:
            seen.append(v)
    return seen

def _unique_components(records):
    seen = []
    for r in records:
        for k in ("source", "target"):
            c = r.get(k, "")
            if c and c not in seen:
                seen.append(c)
    return seen

_SEQ_STEREOTYPES = """
    private import ScalarValues::String;

    // -- spec42-compatible interaction stereotypes -------------------------
    part def InteractionScenario;
    part def Lifeline;

    part def Message {
        attribute label : String;
        ref from : Lifeline;
        ref to   : Lifeline;
    }

    part def SynchronousCall     :> Message;
    part def ReturnMessage       :> Message;
    part def AsynchronousMessage :> Message;
"""

# -- interactions.sysml -- structural architecture -------------------------

def _interactions_file(scenario, interactions):
    label = SCENARIO_LABELS.get(scenario, scenario)
    pkg   = f"CPScoreH1_{scenario}_Structure"

    iface_names = _ordered_unique(interactions, "interaction")
    comps       = _unique_components(interactions)

    calls    = {c: [] for c in comps}
    provides = {c: [] for c in comps}
    for intr in interactions:
        name = intr["interaction"]
        if name not in calls[intr["source"]]:
            calls[intr["source"]].append(name)
        if name not in provides[intr["target"]]:
            provides[intr["target"]].append(name)

    lines = [
        f"// H1 C6 -- {scenario}: {label}",
        f"// Structural architecture: components, interfaces, and connections",
        f"// Generated by experiments/H1/conditions/gen_sysml.py",
        f"// Visualise with: InterconnectionView in spec42 SysML v2 Editor",
        "",
        f"package {pkg} {{",
        "",
        f"    // -- Interface (port type) definitions -- one per interaction --",
    ]

    for name in iface_names:
        lines.append(f"    port def {_cap(name)};  // {name}()")

    lines.append("")
    lines.append(f"    // -- Component part definitions ----------------------")

    for comp in comps:
        lines.append(f"    part def {comp} {{")
        for name in calls[comp]:
            lines.append(f"        port calls{_cap(name)}    : {_cap(name)};  // calls {name}()")
        for name in provides[comp]:
            lines.append(f"        port provides{_cap(name)} : {_cap(name)};  // provides {name}()")
        lines.append(f"    }}")
        lines.append("")

    lines.append(f"    // -- System: component instances and connections ------")
    lines.append(f"    part {_ll(scenario)}System {{")
    lines.append("")

    for comp in comps:
        lines.append(f"        part {_ll(comp)} : {comp};")
    lines.append("")

    for intr in interactions:
        src   = _ll(intr["source"])
        tgt   = _ll(intr["target"])
        name  = intr["interaction"]
        iface = _cap(name)
        lines.append(f"        connection : {iface}")
        lines.append(f"            connect {src}::calls{iface}")
        lines.append(f"            to      {tgt}::provides{iface};")
        lines.append("")

    lines.append("    }")
    lines.append("}")
    return "\n".join(lines) + "\n"

# -- sequence.sysml -- interaction scenario --------------------------------

def _sequence_file(scenario, seq_events, interactions):
    label = SCENARIO_LABELS.get(scenario, scenario)
    pkg   = f"CPScoreH1_{scenario}_Sequence"
    comps = _unique_components(seq_events + interactions)

    lines = [
        f"// H1 C6 -- {scenario}: {label}",
        f"// Ordered execution sequence (from sequence.json)",
        f"// Generated by experiments/H1/conditions/gen_sysml.py",
        f"// Visualise with: SequenceView in spec42 SysML v2 Editor",
        "",
        f"package {pkg} {{",
        _SEQ_STEREOTYPES,
        f"    part def {scenario}Sequence :> InteractionScenario {{",
        "",
    ]

    for comp in comps:
        lines.append(f"        part {_ll(comp)} : Lifeline;  // {comp}")
    lines.append("")

    if not seq_events:
        lines.append("        // (no sequence events -- empty trace for this condition)")
    for ev in seq_events:
        step = ev["step"]
        src  = _ll(ev["source"])
        tgt  = _ll(ev["target"])
        intr = ev["interaction"]
        note = ev.get("note", "").strip()

        lines.append(f"        part m{step} : SynchronousCall {{")
        lines.append(f"            ref from : Lifeline = {src};")
        lines.append(f"            ref to   : Lifeline = {tgt};")
        lines.append(f"            attribute :>> label = \"{step}. {intr}\";")
        if note:
            lines.append(f"            // Note: {note}")
        lines.append(f"        }}")
        lines.append("")

    lines.append("    }")
    lines.append("}")
    return "\n".join(lines) + "\n"

# -- views.sysml -----------------------------------------------------------

def _views_file(scenario, has_seq, has_intr):
    label      = SCENARIO_LABELS.get(scenario, scenario)
    struct_pkg = f"CPScoreH1_{scenario}_Structure"
    seq_pkg    = f"CPScoreH1_{scenario}_Sequence"

    lines = [
        f"// H1 C6 -- {scenario}: {label}",
        f"// Views for spec42 SysML v2 Editor -- open this file and use the View tab",
        f"// Generated by experiments/H1/conditions/gen_sysml.py",
        "",
        f"package H1_{scenario}_Views {{",
    ]
    if has_intr:
        lines.append(f"    import {struct_pkg}::*;")
    if has_seq:
        lines.append(f"    import {seq_pkg}::*;")
    lines.append("")

    if has_intr:
        lines.append(f"    // Structural architecture: components, interfaces, connections")
        lines.append(f"    view {scenario}StructView : InterconnectionView {{")
        lines.append(f"        expose {struct_pkg}::{_ll(scenario)}System;")
        lines.append(f"    }}")
        lines.append("")

    if has_seq:
        lines.append(f"    // Ordered execution sequence")
        lines.append(f"    view {scenario}SeqView : SequenceView {{")
        lines.append(f"        expose {seq_pkg}::{scenario}Sequence;")
        lines.append(f"    }}")
        lines.append("")

    lines.append("}")
    return "\n".join(lines) + "\n"

# -- Main ------------------------------------------------------------------

def main():
    parser = argparse.ArgumentParser(description=__doc__.splitlines()[0])
    parser.add_argument("--scenario", choices=ALL_SCENARIOS,
                        help="Generate for one scenario only (default: all)")
    args = parser.parse_args()

    scenarios = [args.scenario] if args.scenario else ALL_SCENARIOS

    print(f"Generating SysML v2 for scenarios: {', '.join(scenarios)}")
    print(f"Output root: {OUT_ROOT.relative_to(ROOT)}")
    print()

    for scenario in scenarios:
        interactions = _load_json(C6_DIR / scenario / "interactions.json")
        seq_events   = _load_json(C6_DIR / scenario / "sequence.json")

        if not interactions and not seq_events:
            print(f"  [SKIP] {scenario} -- no C6 output files found")
            continue

        out_dir = OUT_ROOT / scenario
        out_dir.mkdir(parents=True, exist_ok=True)

        files = {
            "interactions.sysml": _interactions_file(scenario, interactions),
            "sequence.sysml":     _sequence_file(scenario, seq_events, interactions),
            "views.sysml":        _views_file(scenario, bool(seq_events), bool(interactions)),
        }

        for name, content in files.items():
            path = out_dir / name
            path.write_text(content, encoding="utf-8")
            print(f"  {(out_dir / name).relative_to(ROOT)}")

        print()

    print("Done. To visualise: open views.sysml in VS Code with the spec42")
    print("SysML v2 Editor plugin active, then open the View tab.")

if __name__ == "__main__":
    main()
