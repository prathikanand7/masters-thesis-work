"""
run_c2_static_only.py — H1 Condition 2: Static call-graph only
---------------------------------------------------------------
Reads sequence_dependency_table.csv — generated by static analysis of the
CPSCore codebase (Renaissance/Understand → Neo4j → export). Maps file paths
to top-level component names, filters to each scenario's component set.

No runtime trace data is used. Neo4j is queried as a fallback if the CSV
is absent (requires Docker to be running).

Usage:
    python run_c2_static_only.py --scenario S1
    python run_c2_static_only.py --all

Output: experiments/H1/conditions/C2_static_only/<scenario>/elements.json
"""
import argparse
import csv
import pathlib
import sys

sys.path.insert(0, str(pathlib.Path(__file__).parent))
from _utils import (ROOT, SCENARIOS, SCENARIO_COMPONENTS, SCENARIO_PRIMARY,
                    Element, save_elements, elements_path,
                    path_to_component, neo4j_driver)

LABEL      = "C2_static_only"
STATIC_CSV = ROOT / "sequence_dependency_table.csv"

# ── CSV-based extraction (primary) ──────────────────────────────────────────
def load_from_csv(scenario: str) -> set[Element]:
    """
    Parse sequence_dependency_table.csv (static call-graph export).
    Columns: Sequence, EventName, ClientComponent, ClientFunction,
             ServerComponent, ServerFunction, RelationshipType, EventTimestamp
    ClientComponent / ServerComponent are file paths here, not module names.
    """
    allowed  = SCENARIO_COMPONENTS[scenario]
    elements: set[Element] = set()

    primary = SCENARIO_PRIMARY[scenario]
    with open(STATIC_CSV, newline="") as f:
        reader = csv.DictReader(f)
        for row in reader:
            src_comp = path_to_component(row.get("ClientComponent", ""))
            tgt_comp = path_to_component(row.get("ServerComponent",  ""))
            if src_comp is None or tgt_comp is None or src_comp == tgt_comp:
                continue
            if src_comp != primary or tgt_comp not in allowed:
                continue  # only edges driven by the primary component
            interaction = row.get("EventName", "").strip()
            if interaction:
                elements.add(Element(src_comp, tgt_comp, interaction))

    return elements

# ── Neo4j-based extraction (fallback) ───────────────────────────────────────
CYPHER = """
MATCH (caller)-[:CppCalls]->(callee)
WHERE (caller.path CONTAINS '/src/' OR caller.path CONTAINS '/include/cpsCore/')
  AND (callee.path CONTAINS '/src/' OR callee.path CONTAINS '/include/cpsCore/')
RETURN DISTINCT
    caller.path       AS callerPath,
    caller.simpleName AS callerName,
    callee.path       AS calleePath,
    callee.simpleName AS calleeName
"""

def load_from_neo4j(scenario: str) -> set[Element]:
    allowed  = SCENARIO_COMPONENTS[scenario]
    elements: set[Element] = set()
    driver   = neo4j_driver()
    try:
        with driver.session() as session:
            for record in session.run(CYPHER):
                src_comp = path_to_component(record["callerPath"] or "")
                tgt_comp = path_to_component(record["calleePath"] or "")
                if not src_comp or not tgt_comp or src_comp == tgt_comp:
                    continue
                if src_comp not in allowed or tgt_comp not in allowed:
                    continue
                interaction = (record["calleeName"] or "").strip()
                if interaction:
                    elements.add(Element(src_comp, tgt_comp, interaction))
    finally:
        driver.close()
    return elements


def run_scenario(scenario: str) -> None:
    if STATIC_CSV.exists():
        elements = load_from_csv(scenario)
        source = "sequence_dependency_table.csv"
    else:
        print("  CSV not found — falling back to Neo4j query")
        elements = load_from_neo4j(scenario)
        source = "Neo4j"

    print(f"  Source: {source}")
    save_elements(elements, elements_path(LABEL, scenario))


def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("--scenario", choices=SCENARIOS)
    parser.add_argument("--all", action="store_true")
    args = parser.parse_args()
    targets = SCENARIOS if args.all else [args.scenario]
    for s in targets:
        print(f"C2 static-only: {s}")
        run_scenario(s)


if __name__ == "__main__":
    main()
